void lcd_address(uint8  page,uint8  column)
{
	cs1=0;
	column=column-1;
	page=page-1;
	transfer_command(0xb0+page);
	transfer_command(((column>>4)&0x0f)+0x10);
	transfer_command(column&0x0f);
}

//===============clear all dot martrics=============
void clear_screen()
{
   uint8 i,j;
	for(j=0;j<16;j++)
	{
		lcd_address(j+1,1);
		for(i=0;i<128;i++)
		{
			transfer_data(0x00);
		}
	}
}


/*=======写指令========*/
void transfer_command(uint16  data1)   
{
	uint8  i;
	cs1=0;
	rs=0;
	for(i=0;i<8;i++) 
	{
		sclk=0;
		Delay1(2);
		if(data1&0x80) sid=1;
		else sid=0;
		sclk=1;
		Delay1(2);
		data1=data1<<=1;	
	}
	cs1=1;
}

/*--------写数据------------*/
void transfer_data(uint16  data1)
{
	uint8  i;
	cs1=0;
	rs=1;
	for(i=0;i<8;i++)
	{
		sclk=0;
		if(data1&0x80) sid=1;
		else sid=0;
		sclk=1;
		data1=data1<<=1;		
	}
	cs1=1;
}




//=============delay time=====================
void delay(uint16  i)               
{
	uint16  j,k;
	for(j=0;j<i;j++)
	for(k=0;k<990;k++);
}

//=============delay time=====================
void Delay1(uint8 i)
{
	uint8  j,k;
	for(j=0;j<i;j++)
	for(k=0;k<10;k++);
}



