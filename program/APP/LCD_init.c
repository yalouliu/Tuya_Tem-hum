void display_string_8x16(uint16  page,uint16  column,uint8 *text)
{
	uint16  i=0,j,k,n;
	cs1=0;
	while(text[i]>0x00)
	{	
		if((text[i]>=0x20)&&(text[i]<=0x7e))
		{
			j=text[i]-0x20;
			for(n=0;n<2;n++)
			{			
				lcd_address(page+n,column);
				for(k=0;k<8;k++)
				{
					transfer_data(ascii_table_8x16[j][k+8*n]);//显示5x7的ASCII字到LCD上，y为页地址，x为列地址，最后为数据
				}
			}
			i++;
			column+=8;
			if(column>127)
			{
				column=0;
				page+=2;
			}
		}
		else
		i++;
	}
}
 

void display_data_8x20(uint8  page,uint8 column,uint8 dat)
{
	uint8  k,n;
	cs1=0;
	
	if(dat >13)  return;
	for(n=0;n<2;n++)
	{			
		lcd_address(page+n,column);
		for(k=0;k<8;k++)
		{
			transfer_data(ascii_table_8x20[dat][k+8*n]);/*显示5x7的ASCII字到LCD上，y为页地址，x为列地址，最后为数据*/
		}
	}
	
}

